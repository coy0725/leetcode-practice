    class Solution {
        public int evalRPN(String[] tokens) {
            Stack<Integer> stack = new Stack<>();
            for (final String token : tokens) {
                switch (token) {
                    case "+":
                    case "-":
                    case "*":
                    case "/":
                        int num2 = stack.pop();//å…ˆå¼¹å‡ºçš„æ˜¯æ“ä½œæ•°2
                        int num1 = stack.pop();//åå¼¹å‡ºçš„æ˜¯æ“ä½œæ•°1
                        stack.push(calculate(num1, num2, token));
                        break;
                    default:
                        stack.push(Integer.parseInt(token));
                }
            }
            return stack.pop();
        }

        private Integer calculate(int num1, int num2, String operator) {
            switch (operator) {
                case "+":
                    return num1 + num2;
                case "-":
                    return num1 - num2;
                case "*":
                    return num1 * num2;
                case "/":
                    return num1 / num2;
                default:
                    return 0;
            }
        }
    }
